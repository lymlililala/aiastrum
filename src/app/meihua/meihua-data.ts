// ===== 体用关系分析 =====
export interface TiYongRelation {
  type: "用生体" | "体生用" | "体克用" | "用克体" | "比和";
  level: "大吉" | "中吉" | "吉" | "平" | "泄气" | "凶";
  summary: string;
  detail: string;
  advice: string;
}

export function analyzeTiYong(ti: WuXing, yong: WuXing): TiYongRelation {
  if (ti === yong) {
    return {
      type: "比和",
      level: "吉",
      summary: "体用比和，诸事平稳",
      detail: "体卦与用卦五行相同，两者和谐共振，事情发展顺其自然，无大吉亦无大凶，平稳推进即可。",
      advice: "顺其自然，不必强求，耐心等待时机成熟。",
    };
  }
  if (WUXING_SHENG[yong] === ti) {
    return {
      type: "用生体",
      level: "大吉",
      summary: "用生体，大吉！外有援助",
      detail: "用卦生扶体卦，代表外界环境、他人或所占之事对自己有利，有贵人相助，事情顺水推舟，结果佳。",
      advice: "大胆行事，主动出击，贵人就在左右，善加利用外部资源。",
    };
  }
  if (WUXING_SHENG[ti] === yong) {
    return {
      type: "体生用",
      level: "泄气",
      summary: "体生用，耗费精力",
      detail: "体卦生扶用卦，代表自身付出多而回报少，虽然事情可以推进，但主体方耗费精力，得不偿失。",
      advice: "量力而行，适当收敛，避免过度付出，注意保存实力。",
    };
  }
  if (WUXING_KE[ti] === yong) {
    return {
      type: "体克用",
      level: "中吉",
      summary: "体克用，需付努力可成",
      detail: "体卦克制用卦，代表自身力量可以驾驭所占之事，但需主动付出力量。事情可成，但需努力。",
      advice: "主动出击，掌握主动权，以己之力推动事情向好发展，切勿消极等待。",
    };
  }
  // 用克体
  return {
    type: "用克体",
    level: "凶",
    summary: "用克体，阻力较大，需谨慎",
    detail: "用卦克制体卦，代表外界环境、他人或所占之事对自身不利，阻力较大，行事需格外谨慎。",
    advice: "此时不宜强行推进，宜守不宜攻，静待时机转变，或从长计议换个方向。",
  };
}

