/**
 * 获取汉字的康熙字典笔画数
 * 如果字典中没有记录，则使用简体字笔画数作为降级方案
 */
export function getKangxiStrokes(char: string): number {
  if (KANGXI_STROKES[char] !== undefined) {
    return KANGXI_STROKES[char]!;
  }
  // 降级方案：返回一个合理的估算值（基于 Unicode 属性）
  // 对于字典未收录的字，使用保守估算
  const code = char.charCodeAt(0);
  if (code >= 0x4e00 && code <= 0x9fff) {
    // 基于笔画复杂度的简单估算（不精确，仅作降级）
    return Math.max(1, Math.min(20, Math.floor((code - 0x4e00) / 500) + 2));
  }
  return 1;
}

/**
 * 获取姓名各字的笔画数组
 */
export function getNameStrokes(name: string): number[] {
  return Array.from(name).map(getKangxiStrokes);
}
