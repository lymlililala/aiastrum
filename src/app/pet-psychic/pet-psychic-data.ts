export function buildPetPsychicResult(petName: string, petType: PetType): PetPsychicResult {
  const today = new Date().toDateString();
  const rand = seededRandom(petName + petType + today);

  const cardIndex = Math.floor(rand() * PET_TAROT_CARDS.length);
  const card = PET_TAROT_CARDS[cardIndex]!;

  const loveLevel = Math.floor(rand() * 30) + 70;   // 70-100
  const moodLevel = Math.floor(rand() * 60) + 40;   // 40-100
  const hungerLevel = Math.floor(rand() * 80) + 20; // 20-100

  const secretDesire = SECRET_DESIRES[Math.floor(rand() * SECRET_DESIRES.length)]!;
  const todayPlan = TODAY_PLANS[Math.floor(rand() * TODAY_PLANS.length)]!;

  const fullMessage = buildFullMessage(petName, petType, card, hungerLevel);

  return {
    petName,
    petType,
    card,
    fullMessage,
    secretDesire,
    todayPlan,
    loveLevel,
    moodLevel,
    hungerLevel,
  };
}

function buildFullMessage(
  petName: string,
  petType: PetType,
  card: typeof PET_TAROT_CARDS[0],
  hunger: number
): string {
  const typeWord = petType === "猫咪" ? "本猫" : petType === "狗狗" ? "本狗" : `${petName}`;
  const hungerNote = hunger > 70 ? "（PS：${typeWord}有点饿，主人你懂的）" : "";
  return `${petName}今天抽到了「${card.name}」——${card.petVoice}${hungerNote}`;
}
